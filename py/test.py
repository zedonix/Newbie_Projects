furniture = ["table", "chair", "rack", "shelf"]

print(furniture[0:4])
# ['table', 'chair', 'rack', 'shelf']

print(furniture[1:3])
# ['chair', 'rack']

print(furniture[0:-1])
# ['table', 'chair', 'rack']

print(furniture[:2])
# ['table', 'chair']

print(furniture[1:])
# ['chair', 'rack', 'shelf']

print(furniture[:])
# ['table', 'chair', 'rack', 'shelf']
